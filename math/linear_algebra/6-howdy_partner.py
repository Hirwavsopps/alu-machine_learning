#!/usr/bin/env python3
"""Module for array concatenation."""


def cat_arrays(arr1, arr2):
    """Concatenate two arrays.

    Args:
        arr1: First list of ints/floats.
        arr2: Second list of ints/floats.

    Returns:
        list: New list with arr2 appended to arr1.
    """
    return arr1 + arr2
